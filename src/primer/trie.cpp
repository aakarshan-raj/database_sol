template <class T>
auto Trie::Get(std::string_view key) const -> const T * {
  if (root_ == nullptr) {
    return nullptr;
  }
  auto temp_node = root_;
  for (auto &c : key) {
    if (!(temp_node->HasChild(c))) {
      return nullptr;
    }
    temp_node = temp_node->GetChildNode(c);
    if (temp_node == nullptr) {
      return nullptr;
    }
  }
  if (key.empty()) {
    temp_node = root_->GetChildNode(*key.begin());
  }
  if (!temp_node->is_value_node_) {
    return nullptr;
  }

  auto node_value_pointer = std::dynamic_pointer_cast<const TrieNodeWithValue<T>>(temp_node);
  if ((!node_value_pointer)) {
    return nullptr;
  }
  return node_value_pointer->value_.get();
}

